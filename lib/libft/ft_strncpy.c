#include "libft.h"

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	char *s;

	s = dest;
	while (n > 0 && *src != '\0')
	{
		*s++ = *src++;
		--n;
	}
	while (n > 0)
	{
		*s++ = '\0';
		--n;
	}
	return (dest);
}
