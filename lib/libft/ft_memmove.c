#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	unsigned char		*d;
	const unsigned char *s;

	d = (unsigned char *)dst;
	s = (const unsigned char *)src;
	if (d >= s)
	{
		d += len;
		s += len;
		while (len)
		{
			*--d = *--s;
			len--;
		}
	}
	else
		ft_memcpy(dst, src, len);
	return (dst);
}
